--[[A key-value pair is a way of storing information where each piece of data (the value) has a unique name (the key) that identifies it. Think of 
it like a real dictionary where you look up a word (the key) to find its definition (the value).]]

local player = {
    ["name"] = "Alex",
    ["level"] = 15,
    ["health"] = 100
}

--In this example, "name", "level", and "health" are the keys, while "Alex", 15, and 100 are their corresponding values.