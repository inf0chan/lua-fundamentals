--The type() function takes any value or variable as input and returns a string that tells you what data type it is. 
--Here's how it works:

playerScore = 100
playerName = "Codmaster"
isReady = true
emptyValue = nil

print(type(playerScore))  -- outputs: number
print(type(playerName))   -- outputs: string
print(type(isReady))      -- outputs: boolean
print(type(emptyValue))   -- outputs: nil